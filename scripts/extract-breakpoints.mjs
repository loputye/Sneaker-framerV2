// Regenerate src/live-breakpoints.css from the published page's media rules.
// - .framer-a1uI3 (scene scope on live) → .framer-9Yclq.framer-9Yclq (doubled
//   so it outranks the exported bundle's runtime-injected base rules)
// - preset/footer hashes copied verbatim (they exist in the clone DOM)
// Only max-width media blocks are taken: desktop base already matches.
import { readFileSync, writeFileSync } from 'fs';

const html = readFileSync('/home/live-site.html', 'utf8');
const styles = [...html.matchAll(/<style[^>]*>([\s\S]*?)<\/style>/gi)].map((m) => m[1]).join('\n');

const rules = []; // {media, sel, body}
let i = 0;
while ((i = styles.indexOf('@media', i)) !== -1) {
  const braceStart = styles.indexOf('{', i);
  const cond = styles.slice(i + 6, braceStart).trim();
  let depth = 1, j = braceStart + 1;
  while (j < styles.length && depth > 0) { if (styles[j] === '{') depth++; else if (styles[j] === '}') depth--; j++; }
  const inner = styles.slice(braceStart + 1, j - 1);
  i = j;
  if (!/max-width/.test(cond)) continue;
  // parse flat rules inside
  let k = 0;
  while (k < inner.length) {
    const b = inner.indexOf('{', k);
    if (b === -1) break;
    const sel = inner.slice(k, b).trim();
    let d = 1, e = b + 1;
    while (e < inner.length && d > 0) { if (inner[e] === '{') d++; else if (inner[e] === '}') d--; e++; }
    const body = inner.slice(b + 1, e - 1).trim();
    k = e;
    if (sel && body) rules.push({ media: cond, sel, body });
  }
}

const KEEP_VERBATIM = ['framer-4RHim', 'framer-t3DkE', 'framer-aSX0A', 'framer-F1CzE', 'framer-nZzIb', 'framer-yshUJ'];
const out = [];
for (const r of rules) {
  if (r.sel.includes('.framer-a1uI3')) {
    const sel = r.sel.split(',').map((s) => s.replaceAll('.framer-a1uI3', '.framer-9Yclq.framer-9Yclq')).join(',');
    out.push({ media: r.media, sel, body: r.body });
  } else if (KEEP_VERBATIM.some((h) => r.sel.includes(h))) {
    // double each scope hash so these outrank the bundle's runtime-injected
    // preset base rules of equal specificity
    let sel = r.sel;
    for (const h of KEEP_VERBATIM) sel = sel.replaceAll(`.${h} `, `.${h}.${h} `);
    out.push({ media: r.media, sel, body: r.body });
  }
}

let css = `/* Breakpoint rules extracted from the published page (sneako.framer.website).\n   The exporter drops canvas media queries for nested smart components; these\n   restore them. .framer-a1uI3 (live scene scope) is rescoped to a doubled\n   .framer-9Yclq.framer-9Yclq so the rules outrank the exported bundle's\n   runtime-injected base rules of equal origin. Generated by\n   scripts/extract-breakpoints.mjs — do not hand-edit. */\n`;
let cur = null;
for (const r of out) {
  if (r.media !== cur) { if (cur !== null) css += '}\n'; css += `@media ${r.media}{\n`; cur = r.media; }
  css += `${r.sel}{${r.body}}\n`;
}
if (cur !== null) css += '}\n';
writeFileSync('/home/Sneaker-framer/src/live-breakpoints.css', css);
console.log(`wrote ${out.length} rules across media blocks`);
